a = int(input("Digite o primeiro número inteiro: "))
b = int(input("Digite o segundo número inteiro: "))
real = float(input("Digite o número real: "))

produto = (a * 2) * (b / 2)
soma = (a * 3) + real
cubo = real ** 3

print("O produto do dobro de A com a metade de B é: ",produto)
print("A soma do triplo de A com REAL é: ",soma)
print("O REAL elevado ao cubo é: ",cubo)